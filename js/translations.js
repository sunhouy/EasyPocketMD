    // 语言管理器
    // 暴露到全局
    window.i18n = {
        // 初始化语言
        init: function () {
            // 从本地存储获取语言设置，默认使用中文
            const savedLanguage = localStorage.getItem('vditor_language');
            if (savedLanguage && (savedLanguage === 'zh' || savedLanguage === 'en')) {
                currentLanguage = savedLanguage;
            } else {
                // 默认设置为中文
                currentLanguage = 'zh';
                localStorage.setItem('vditor_language', 'zh');
            }
            document.documentElement.lang = currentLanguage === 'zh' ? 'zh-CN' : 'en';
            return currentLanguage;
        },

        // 获取当前语言
        getLanguage: function () {
            return currentLanguage;
        },

        // 设置语言
        setLanguage: function (lang) {
            if (lang !== 'zh' && lang !== 'en') {
                console.warn('Invalid language:', lang);
                return;
            }
            currentLanguage = lang;
            localStorage.setItem('vditor_language', lang);
            document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
        },

        // 获取翻译文本
        t: function (key) {
            const langTranslations = translations[currentLanguage];
            if (langTranslations && langTranslations[key]) {
                return langTranslations[key];
            }
            // 如果没有找到翻译，返回键名
            console.warn('Translation not found for key:', key);
            return key;
        },

        has: function (key) {
            const langTranslations = translations[currentLanguage];
            return !!(langTranslations && Object.prototype.hasOwnProperty.call(langTranslations, key));
        },

        tOr: function (key, fallback) {
            const langTranslations = translations[currentLanguage];
            if (langTranslations && Object.prototype.hasOwnProperty.call(langTranslations, key)) {
                return langTranslations[key];
            }
            return fallback || key;
        },

    };

