    async function openCropTool(img, modal) {
        if (!img || !img.src) {
            global.showMessage ? global.showMessage('无效的图片', 'error') : alert('无效的图片');
            return;
        }

        blurActiveTextInput();

        var cropModal = createFullscreenCropModal();
        var cropTarget = cropModal.querySelector('.epmd-fs-crop-target');
        var cropStage = cropModal.querySelector('.epmd-fs-crop-stage');
        var cropCloseBtn = cropModal.querySelector('.epmd-fs-crop-close');
        var cropConfirmBtn = cropModal.querySelector('.epmd-fs-crop-confirm');
        var cropCancelBtn = cropModal.querySelector('.epmd-fs-crop-cancel');

        if (!cropTarget || !cropStage || !cropCloseBtn || !cropConfirmBtn || !cropCancelBtn) {
            global.showMessage ? global.showMessage('裁剪界面初始化失败', 'error') : alert('裁剪界面初始化失败');
            return;
        }

        cropModal.style.display = 'flex';
        syncFullscreenCropViewport(cropModal);
        setTimeout(function() {
            if (cropModal.style.display !== 'none') {
                syncFullscreenCropViewport(cropModal);
            }
        }, 120);
        cropModal.epmdCropState = {
            busy: false,
            dirty: false,
            saved: false,
            initialSnapshot: null,
            confirmingClose: false
        };
        setFullscreenCropBusy(cropModal, true, '正在加载全屏裁剪工具...');

        var targetImage = img;
        var toolModal = modal;

        var submitCropAndClose = async function() {
            if (!targetImage || !activeCropper) return;
            var cropState = cropModal.epmdCropState;
            if (cropState) {
                cropState.saved = false;
            }

            setFullscreenCropBusy(cropModal, true, '正在裁剪并上传...');

            try {
                var oldSrc = targetImage.getAttribute('src') || '';
                var oldMeta = getImageMeta(targetImage);
                if (typeof activeCropper.getCroppedCanvas !== 'function') {
                    throw new Error('当前裁剪器不支持导出，请刷新后重试');
                }

                var canvas = activeCropper.getCroppedCanvas({
                    imageSmoothingEnabled: true,
                    imageSmoothingQuality: 'high',
                    fillColor: '#ffffff'
                });

                if (!canvas) {
                    throw new Error('生成裁剪结果失败');
                }

                var blob = await blobFromCanvas(canvas, 'image/png', 0.95);
                var uploadedUrl = await uploadCroppedImage(blob, oldSrc);

                targetImage.src = uploadedUrl;
                targetImage.removeAttribute('srcset');
                targetImage.dataset.epmdCropped = '1';
                applyImageStyle(targetImage, oldMeta.width || 0, oldMeta.rotate || 0);

                var replaced = replaceImageSourceInEditor(oldSrc, uploadedUrl, oldMeta.width || 0, oldMeta.rotate || 0, targetImage.getAttribute('alt') || '');
                if (!replaced && !editorContainsImageUrl(uploadedUrl)) {
                    throw new Error('裁剪后链接替换失败');
                }

                if (cropState) {
                    cropState.saved = true;
                    cropState.dirty = false;
                }
                hideFullscreenCropModal();
                if (toolModal) {
                    toolModal.style.display = 'none';
                }
                global.showMessage ? global.showMessage('裁剪完成，已替换为云端新图片', 'success') : alert('裁剪完成，已替换为云端新图片');
            } catch (error) {
                console.error('裁剪上传失败', error);
                global.showMessage ? global.showMessage('裁剪失败: ' + error.message, 'error') : alert('裁剪失败: ' + error.message);
            } finally {
                if (cropModal.style.display !== 'none') {
                    setFullscreenCropBusy(cropModal, false, '拖动裁剪框后点击确认裁剪');
                }
            }
        };

        cropCloseBtn.onclick = function() {
            requestCloseFullscreenCropModal(cropModal, submitCropAndClose);
        };

        cropCancelBtn.onclick = function() {
            requestCloseFullscreenCropModal(cropModal, submitCropAndClose);
        };

        try {
            var Cropper = await loadCropperLibrary();
            destroyActiveCropper(cropModal);

            var sourceSrc = targetImage.getAttribute('src') || targetImage.src || '';
            var resolvedSrc = sourceSrc;

            if (typeof global.resolveResourceUrl === 'function') {
                resolvedSrc = global.resolveResourceUrl(sourceSrc, getNativeAwareResolveBase());
            }

            if (global.ResourceLoader && typeof global.ResourceLoader.getLocalBlobUrl === 'function') {
                var localBlobUrl = await global.ResourceLoader.getLocalBlobUrl(sourceSrc);
                if (!localBlobUrl && resolvedSrc !== sourceSrc) {
                    localBlobUrl = await global.ResourceLoader.getLocalBlobUrl(resolvedSrc);
                }
                if (localBlobUrl) {
                    resolvedSrc = localBlobUrl;
                }
            }

            cropTarget.crossOrigin = 'anonymous';

            try {
                await new Promise(function(resolve, reject) {
                    cropTarget.onload = function() { resolve(); };
                    cropTarget.onerror = function() { reject(new Error('图片解析失败')); };
                    cropTarget.src = resolvedSrc;
                });
            } catch (loadError) {
                if (!resolvedSrc || /^(blob:|data:|local:|content:|file:)/i.test(resolvedSrc)) {
                    throw loadError;
                }

                var imageResponse = await fetch(resolvedSrc, { mode: 'cors' });
                if (!imageResponse.ok) {
                    throw new Error('图片加载失败: ' + imageResponse.status);
                }

                var imageBlob = await imageResponse.blob();
                var objectUrl = URL.createObjectURL(imageBlob);
                cropModal.dataset.epmdCropObjectUrl = objectUrl;
                await new Promise(function(resolve, reject) {
                    cropTarget.onload = function() { resolve(); };
                    cropTarget.onerror = function() { reject(new Error('图片解析失败')); };
                    cropTarget.src = objectUrl;
                });
            }

            activeCropper = new Cropper(cropTarget, {
                viewMode: 1,
                dragMode: 'move',
                autoCropArea: 1,
                responsive: true,
                background: false,
                checkCrossOrigin: false,
                checkOrientation: false,
                movable: true,
                zoomable: true,
                scalable: false,
                rotatable: false,
                guides: true,
                center: true,
                highlight: true,
                cropBoxMovable: true,
                cropBoxResizable: true,
                toggleDragModeOnDblclick: false,
                container: cropStage,
                ready: function() {
                    var state = cropModal.epmdCropState;
                    if (!state) return;
                    state.initialSnapshot = captureCropSnapshot(activeCropper);
                    state.dirty = false;
                    state.saved = false;
                },
                crop: function() {
                    var state = cropModal.epmdCropState;
                    if (!state || !state.initialSnapshot || state.saved) return;
                    var currentSnapshot = captureCropSnapshot(activeCropper);
                    state.dirty = hasCropSnapshotChanged(state.initialSnapshot, currentSnapshot);
                },
                zoom: function() {
                    var state = cropModal.epmdCropState;
                    if (!state || !state.initialSnapshot || state.saved) return;
                    var currentSnapshot = captureCropSnapshot(activeCropper);
                    state.dirty = hasCropSnapshotChanged(state.initialSnapshot, currentSnapshot);
                }
            });
            activeCropperHost = cropModal;

            setFullscreenCropBusy(cropModal, false, '拖动裁剪框后点击确认裁剪');
        } catch (error) {
            console.error('裁剪工具加载失败', error);
            hideFullscreenCropModal();
            global.showMessage ? global.showMessage('裁剪工具加载失败: ' + error.message, 'error') : alert('裁剪工具加载失败: ' + error.message);
            return;
        }

        cropConfirmBtn.onclick = submitCropAndClose;
    }

    function deleteImage(img, modal) {
        if (!global.vditor || typeof global.vditor.getValue !== 'function' || typeof global.vditor.setValue !== 'function') {
            global.showMessage ? global.showMessage('删除失败', 'error') : alert('删除失败');
            return;
        }

        var src = img.getAttribute('src') || '';
        if (!src) return;

        var raw = global.vditor.getValue() || '';
        var srcNorm = normalizeUrl(src);
        var updated = null;

        // Remove Markdown image syntax: ![alt](url)
        var mdRegex = /!\[[^\]]*\]\([^)\s]+(?:\s+"[^"]*")?\)\s*/g;
        updated = replaceFirstMatch(raw, mdRegex, function(match) {
            var matchedText = match[0] || '';
            var matchUrl = matchedText.match(/\(([^)]+)\)/);
            if (!matchUrl) return null;
            var url = normalizeUrl(matchUrl[1].split(/\s+/)[0]);
            if (!urlsMatch(url, srcNorm)) return null;
            return '';
        });

        // Remove HTML image syntax: <img ... src="...">
        if (updated === null) {
            var htmlRegex = /<img\b[^>]*src=(['"])(.*?)\1[^>]*>\s*/gi;
            updated = replaceFirstMatch(raw, htmlRegex, function(match) {
                var matchUrl = normalizeUrl(match[2]);
                if (!urlsMatch(matchUrl, srcNorm)) return null;
                return '';
            });
        }

        if (updated === null || updated === raw) {
            // Fallback: remove the clicked image element directly in editor DOM if available.
            if (img && img.parentNode) {
                img.parentNode.removeChild(img);
                var latest = global.vditor.getValue() || '';
                if (latest !== raw) {
                    applyEditorValue(latest);
                    closeModal();
                    global.showMessage ? global.showMessage('图片已删除', 'success') : alert('图片已删除');
                    return;
                }
            }
            global.showMessage ? global.showMessage('未找到该图片', 'error') : alert('未找到该图片');
            return;
        }

        applyEditorValue(updated);
        closeModal();
        global.showMessage ? global.showMessage('图片已删除', 'success') : alert('图片已删除');
    }

    async function runOCR(img, modal) {
        var src = img.getAttribute('src');
        if (!src) {
            global.showMessage ? global.showMessage('未找到图片地址', 'error') : alert('未找到图片地址');
            return;
        }

        var resolvedSrc = (typeof global.resolveResourceUrl === 'function')
            ? global.resolveResourceUrl(src, getNativeAwareResolveBase())
            : src;

        var statusDiv = modal.querySelector('.epmd-ocr-status');
        var btn = modal.querySelector('.epmd-ocr-run');
        statusDiv.textContent = 'OCR 识别中...';
        btn.disabled = true;

        try {
            var langSelect = modal.querySelector('.epmd-ocr-lang');
            var apiUrl = (global.getApiBaseUrl ? global.getApiBaseUrl() : 'api') + '/convert/ocr';
            var ocrRes = await fetch(apiUrl, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    imageUrl: resolvedSrc,
                    fallbackOcrApi: OCR_API,
                    lang: langSelect ? langSelect.value : 'chi_tra+chi_sim+eng'
                })
            });
            if (!ocrRes.ok) {
                throw new Error('OCR 请求失败: ' + ocrRes.status);
            }

            var data = await ocrRes.json();
            var text = (data && data.data && data.data.text ? String(data.data.text) : '').trim();
            if (!text) {
                text = '无识别结果';
            }

            if (navigator.clipboard && text !== '无识别结果') {
                try {
                    await navigator.clipboard.writeText(text);
                } catch (e) {
                    // Clipboard can fail on non-secure context.
                }
            }

            var shouldInsert = false;
            if (text !== '无识别结果') {
                if (typeof global.customConfirm === 'function') {
                    shouldInsert = await global.customConfirm('OCR 完成。已复制识别结果到剪贴板。是否插入识别文本到当前光标位置？\n\n' + text.slice(0, 120) + '...');
                } else {
                    shouldInsert = global.confirm('OCR 完成。已复制识别结果到剪贴板。是否插入识别文本到当前光标位置？');
                }
            }

            if (shouldInsert && global.vditor && typeof global.vditor.insertValue === 'function') {
                global.vditor.insertValue('\n\n' + text + '\n\n');
            }

            statusDiv.textContent = text === '无识别结果' ? '无识别结果' : ('识别完成，共 ' + text.length + ' 字');
        } catch (error) {
            console.error('OCR 失败', error);
            statusDiv.textContent = 'OCR 失败: ' + error.message;
        } finally {
            btn.disabled = false;
        }
    }

    function bindImageClickEvents() {
        if (document.body.dataset.epmdImageToolsBound === '1') return;
        document.body.dataset.epmdImageToolsBound = '1';

        var root = document.getElementById('vditor');
        if (!root) return;

        var observer = new MutationObserver(function() {
            scheduleImageBindings();
        });
        observer.observe(root, {
            childList: true,
            subtree: true
        });

        scheduleImageBindings();
    }

    function scheduleImageBindings() {
        if (updateTimer) {
            clearTimeout(updateTimer);
        }
        updateTimer = setTimeout(function() {
            updateTimer = null;
            bindAllImageEvents();
        }, 120);
    }

    function bindAllImageEvents() {
        if (suspendObserver) return;
        var images = getTargetImages();
        images.forEach(function(img) {
            if (img.dataset.epmdToolsBound === '1') return;
            img.dataset.epmdToolsBound = '1';
            img.style.cursor = 'pointer';

            img.addEventListener('touchstart', function(e) {
                openImageToolsFromInteraction(e, img);
            }, { passive: false });

            img.addEventListener('mousedown', function(e) {
                if (typeof e.button === 'number' && e.button !== 0) return;
                openImageToolsFromInteraction(e, img);
            });

            img.addEventListener('click', function(e) {
                // touchstart/mousedown already handled opening; swallow delayed click.
                if (Date.now() - lastImageToolOpenAt < 450) {
                    e.preventDefault();
                    e.stopPropagation();
                    return;
                }
                openImageToolsFromInteraction(e, img);
            });
        });
    }

    function initInlineImageTools() {
        bindImageClickEvents();
    }

