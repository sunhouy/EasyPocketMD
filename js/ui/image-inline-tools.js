    function normalizeUrl(url) {
        if (!url) return '';
        try {
            return decodeURIComponent(String(url)).replace(/&amp;/g, '&').trim();
        } catch (e) {
            return String(url).replace(/&amp;/g, '&').trim();
        }
    }

    function toComparableUrl(url) {
        var normalized = normalizeUrl(url);
        if (!normalized) return { raw: '', path: '', file: '' };

        var path = '';
        try {
            var u = new URL(normalized, (typeof window !== 'undefined' ? window.location.href : 'http://localhost'));
            path = normalizeUrl(u.pathname + (u.search || ''));
        } catch (e) {
            path = normalized.replace(/^https?:\/\/[^/]+/i, '');
        }

        var cleanPath = path.split('#')[0];
        var file = cleanPath.split('?')[0].split('/').pop() || '';
        return { raw: normalized, path: cleanPath, file: file };
    }

    function urlsMatch(left, right) {
        var a = toComparableUrl(left);
        var b = toComparableUrl(right);
        if (!a.raw || !b.raw) return false;
        if (a.raw === b.raw) return true;
        if (a.path && b.path && a.path === b.path) return true;
        if (a.file && b.file && a.file === b.file) {
            // Fallback for absolute-vs-relative URLs generated by the editor runtime.
            return true;
        }
        return false;
    }

    function htmlEscape(text) {
        return String(text || '')
            .replace(/&/g, '&amp;')
            .replace(/</g, '&lt;')
            .replace(/>/g, '&gt;')
            .replace(/"/g, '&quot;');
    }

